fn main() {
    // declare variables
    let name = "John DOe";
    println!("My name is {}", name);

    let value: bool = false;
    println!("My value is {}", value);

    // mutable variables

    let mut price = 100;
    println!("Price is {}", price);
    price = 120;
    println!("New price is {}", price);

}
