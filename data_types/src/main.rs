fn main() {
    // char value
    let x:char = 'a';
    println!("Char value is {}", x);
    // bool value
    let y:bool = false;
    println!("Boolean value is {}", y);
    // float value with single precision
    let z:f32 = 23.11;
    println!("Float value is {}", z);
    // float value with double precision
    let p:f64 = 24.1133232323;
    println!("Float value is {}", p);

}
